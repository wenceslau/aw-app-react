let score = 8;

if (score > 0) {
    console.log('Final score: ', score);
}

if (score <= 4) {
    console.log('Bronze medal!');
} else if (score <= 8) {
    console.log('Silver medal!');
} else {
    console.log('Gold medal!');
}

if (score > 8) {
    console.log('You win!');
} else {
    console.log('You lose!');
}