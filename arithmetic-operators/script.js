// Adição +
const soma = 1 + 1;
console.log('Adição: 1 + 1 =', soma);

// Subtração -
const sub = 10 - 5;
console.log('Subtração: 10 - 5 =', sub);

// Multiplicação *
const mult = 2 * 2;
console.log('Multiplicação: 2 + 2 =', mult);

// Divisão /
const div = 100 / 2;
console.log('Divisão: 100 / 2 =', div);

// Potência **
const pot = 10 ** 2;
console.log('Potência: 10 ** 2 =', pot);

// Módulo %
const mod = 10 % 3;
console.log('Módulo: 10 % 3 =', mod);