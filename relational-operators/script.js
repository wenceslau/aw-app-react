// Menor que <
const menor = 10 < 20;
console.log('10 < 20 ?', menor);

// Maior que >
const maior = 10 > 20;
console.log('10 > 20 ?', maior);

// Igual a ===
const igual = 5 == 5;
console.log('5 == 5 ?', igual);

// Diferente de != 
const dif = 'a' != 'b';
console.log('a != b ?', dif);

// Menor ou igual a <=
const menorOuIgual = 10 <= 5;
console.log('10 <= 5 ?', menorOuIgual);

// Maior ou igual a >=
const maiorOuIgual = 10 >= 10;
console.log('10 >= 10 ?', maiorOuIgual);